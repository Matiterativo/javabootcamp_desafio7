package ejercicio_1;

public class Examen {
	
	private int numero;
	private double nota;
	
	public Examen(int numero, double nota) {
		this.numero = numero;
		this.nota = nota;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public double getNota() {
		return nota;
	}

	public void setNota(double nota) {
		this.nota = nota;
	}
	
	
	
	

}
